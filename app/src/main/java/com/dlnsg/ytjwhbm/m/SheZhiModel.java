package com.dlnsg.ytjwhbm.m;

public class SheZhiModel {

    private int pic;

    private String name;

    public SheZhiModel(int pic, String name) {
        this.pic = pic;
        this.name = name;
    }

    public int getPic() {
        return pic;
    }

    public void setPic(int pic) {
        this.pic = pic;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
