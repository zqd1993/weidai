    private List<SpanTextHaoJieView.BaseSpanModel> createSpanTexts() {
        List<SpanTextHaoJieView.BaseSpanModel> spanModels = new ArrayList<>();
        SpanTextHaoJieView.ClickSpanModel spanModel = new SpanTextHaoJieView.ClickSpanModel();
        SpanTextHaoJieView.TextSpanModel textSpanModel = new SpanTextHaoJieView.TextSpanModel();
        textSpanModel.setContent("为了保障软件服务的安全、运营的质量及效率，我们会收集您的设备信息和服务日志，具体内容请参照隐私条款；" +
                "为了预防恶意程序，确保运营质量及效率，我们会收集安装的应用信息或正在进行的进程信息。" +
                "如果未经您的授权，我们不会使用您的个人信息用于您未授权的其他途径或目的。\n\n" +
                "我们非常重视对您个人信息的保护，您需要同意");
        spanModels.add(textSpanModel);

        spanModel.setContent("《用户注册协议》");
        spanModels.add(spanModel);

        textSpanModel = new SpanTextHaoJieView.TextSpanModel();
        textSpanModel.setContent("和");
        spanModels.add(textSpanModel);

        spanModel = new SpanTextHaoJieView.ClickSpanModel();
        spanModel.setContent("《隐私政策》");
        spanModels.add(spanModel);

        textSpanModel = new SpanTextHaoJieView.TextSpanModel();
        textSpanModel.setContent("，才能继续使用我们的服务。");
        spanModels.add(textSpanModel);
        return spanModels;
    }


