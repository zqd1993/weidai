    private void showDialog() {
        Looper.prepare();
        startPageRemindDialog = new StartPageRemindDialogBeiYong(this);
        startPageRemindDialog.setOnKeyListener(new DialogInterface.OnKeyListener() {
            @Override
            public boolean onKey(DialogInterface dialog, int keyCode, KeyEvent event) {
                if (keyCode == KeyEvent.KEYCODE_BACK && !isResume) {
                    BeiYongStartPageActivity.this.finish();
                    return false;
                }
                return true;
            }
        });
        startPageRemindDialog.setOnListener(new StartPageRemindDialogBeiYong.OnListener() {
            @Override
            public void oneBtnClicked() {
                initUm();
                BeiYongPreferencesOpenUtil.saveBool("isSure", true);
                OpenBeiYongUtil.jumpPage(BeiYongStartPageActivity.this, DlBeiYongActivity.class);
                finish();
            }

            @Override
            public void zcxyClicked() {
                bundle = new Bundle();
                bundle.putString("url", BeiYongHttpApi.ZCXY);
                bundle.putString("biaoti", getResources().getString(R.string.privacy_policy));
                OpenBeiYongUtil.jumpPage(BeiYongStartPageActivity.this, BeiYongJumpH5Activity.class, bundle);
            }

            @Override
            public void twoBtnClicked() {
                finish();
            }

            @Override
            public void ysxyClicked() {
                bundle = new Bundle();
                bundle.putString("url", BeiYongHttpApi.YSXY);
                bundle.putString("biaoti", getResources().getString(R.string.user_service_agreement));
                OpenBeiYongUtil.jumpPage(BeiYongStartPageActivity.this, BeiYongJumpH5Activity.class, bundle);
            }
        });
        startPageRemindDialog.show();
        Looper.loop();
    }

