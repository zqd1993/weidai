    public void jumpWebActivity(GoodsModelKouDai model) {
        if (model != null) {
            webBundle = new Bundle();
            webBundle.putInt("tag", 3);
            webBundle.putString("url", model.getUrls());
            webBundle.putString("title", model.getTitle());
            Router.newIntent(getActivity())
                    .to(WebViewActivityKouDai.class)
                    .data(webBundle)
                    .launch();
        }
    }

    public void initGoodsItemAdapter(List<GoodsModelKouDai> mData) {
        if (goodsItemAdapterKouDai == null) {
            goodsItemAdapterKouDai = new GoodsItemAdapterKouDai(getActivity());
            goodsItemAdapterKouDai.setRecItemClick(new RecyclerItemCallback<GoodsModelKouDai, GoodsItemAdapterKouDai.ViewHolder>() {
                @Override
                public void onItemClick(int position, GoodsModelKouDai model, int tag, GoodsItemAdapterKouDai.ViewHolder holder) {
                    super.onItemClick(position, model, tag, holder);
                    productClick(model);
                }
            });
            goodsItemAdapterKouDai.setHasStableIds(true);
            goodsItemAdapterKouDai.setData(mData);
            rvy.setLayoutManager(new LinearLayoutManager(getActivity()));
            rvy.setHasFixedSize(true);
            rvy.setAdapter(goodsItemAdapterKouDai);
        } else {
            goodsItemAdapterKouDai.setData(mData);
        }
    }

