    @Override
    public void initData(Bundle savedInstanceState) {
        RongYiDaiStatusRongYiDaiBarUtil.setTransparent(this, false);
        backImg.setOnClickListener(v -> finish());
        titleTv.setText("设置");
        phone = SharedRongYiDaiPreferencesUtilis.getStringFromPref("phone");
        phoneTv.setText(phone.replace(phone.substring(3, 7), "****"));
        isPush = SharedRongYiDaiPreferencesUtilis.getBoolFromPref("push");
        switchBtn.setChecked(isPush);
        switchBtn.setmOnCheckedChangeListener(new SwitchRongYiDaiButton.OnCheckedChangeListener() {
            @Override
            public void OnCheckedChanged(boolean isChecked) {
                SharedRongYiDaiPreferencesUtilis.saveBoolIntoPref("push", isChecked);
                RongYiDaiToastUtil.showShort(isChecked ? "开启成功" : "关闭成功");
            }
        });
        logoutBtn.setOnClickListener(v -> {
            normalRongYiDaiDialog = new NormalRongYiDaiDialog(this);
            normalRongYiDaiDialog.setTitle("温馨提示")
                    .setContent("确定退出当前登录")
                    .setCancelText("取消")
                    .setLeftListener(v1 -> {
                        normalRongYiDaiDialog.dismiss();
                    })
                    .setConfirmText("退出")
                    .setRightListener(v2 -> {
                        normalRongYiDaiDialog.dismiss();
                        SharedRongYiDaiPreferencesUtilis.saveStringIntoPref("phone", "");
                        ActivityCollector.finishAll();
                        Router.newIntent(this)
                                .to(LoginActivityRongYiDai.class)
                                .launch();
                    }).show();
        });
    }

